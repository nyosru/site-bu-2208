    public function run(): void
    {
        $this->seedTree(self::CATALOG_TREE, null);
    }

    /**
     * @param array<int|string, mixed> $nodes
     */
    private function seedTree(array $nodes, ?int $parentId): void
    {
        foreach ($nodes as $key => $value) {
            if (is_int($key)) {
                $name = (string) $value;
                $children = [];
            } else {
                $name = (string) $key;
                $children = is_array($value) ? $value : [];
            }

            $cat = Cat::query()->firstOrCreate([
                'name' => $name,
                'cat_up_id' => $parentId,
            ]);

            if ($children !== []) {
                $this->seedTree($children, (int) $cat->id);
            }
        }
    }
